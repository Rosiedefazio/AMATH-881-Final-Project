#!/bin/sh
# -------------------------------------------------
# 1️⃣ Start a virtual X server on display :99
# -------------------------------------------------
Xvfb :99 -screen 0 1024x768x24 &
XVFB_PID=$!

# -------------------------------------------------
# 2️⃣ Export DISPLAY so any GUI‑using program sees it
# -------------------------------------------------
export DISPLAY=:99

# -------------------------------------------------
# 3️⃣ (Optional) give Xvfb a moment to initialise
# -------------------------------------------------
sleep 0.5

# -------------------------------------------------
# 4️⃣ Run the command that the user supplied to `docker run`
# -------------------------------------------------
exec "$@"